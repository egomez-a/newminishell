#include "../includes/libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;
	size_t	i;

	ptr = malloc(count * size);
	if (ptr == NULL)
		return (ptr);
	i = 0;
	while (i < size * count)
	{
		((unsigned char*)ptr)[i] = 0;
		i++;
	}
	return (ptr);
}
